#Integer type
a = 12
b = 100
c = -66
print(a,b,c)
print(type(a))

#Float type
x = 33.3
y = -25.8
z = 205.0
print(x,y,z)
print(type(x))

#Complex type
d=3+5j
print(d)
print(type(d))

#Binary type
e=0B1010
print(e)
print(type(e))

#Hexadecimal
f=0XFF
print(f)
print(type(f))

#Boolean
g=True
print(g)
print(type(g))

#Conversions
h=int(3.5)
print(h)
print(type(h))

i=float("22.5")
print(i)
print(type(i))
